    private ArrayList<users> getDataKaryawan() {
        ArrayList<users> data = new ArrayList();
        try {
            Connection conn = new koneksi().connect();

            Statement st = conn.createStatement();
            String sql = "SELECT u.id, u.username, u.nama_lengkap, u.password, "
                    + "u.email, u.tanggal_lahir, u.tanggal_bergabung, u.aktif, "
                    + "u.nip, u.jabatan_id, u.kelompok_absensi_id,"
                    + "CASE aktif WHEN 1 THEN 'Aktif' ELSE 'Tidak Aktif' END as status_nama, j.nama_jabatan,"
                    + " s.nama FROM users u join jabatan j "
                    + "on u.jabatan_id = j.id join kelompok_absensi s "
                    + "on u.kelompok_absensi_id = s.id;";
            PreparedStatement preparedStatement = conn.prepareStatement(sql);

            int idx = 0;
            ResultSet rs = preparedStatement.executeQuery();
            while (rs.next()) {
                users usr = new users(rs.getInt("id"), rs.getString("username"),rs.getString("nama_lengkap"),
                        rs.getString("password"),rs.getString("email"),rs.getDate("tanggal_lahir"), rs.getDate("tanggal_bergabung"), 
                        rs.getInt("aktif"), rs.getString("nip"),rs.getInt("jabatan_id"),rs.getInt("kelompok_absensi_id"),
                        rs.getString("status_nama"),rs.getString("nama_jabatan"),rs.getString("nama"));
                data.add(usr);
//                jComboBox1.addItem(rs.getString("nama_lengkap")); //Name Column Value
//                IDHolderManager.put(idx, rs.getInt("id")); //ID Column Value
                idx++;
            }
            st.close();
            conn.close();
        } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null,
                        e.getMessage(),
                        "Gagal",
                        JOptionPane.ERROR_MESSAGE);
                    
        }
        return data;
    }        
    
