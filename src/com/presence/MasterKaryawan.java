    private void fillDataTable() {
        ArrayList<users> data = getDataKaryawan();
        DefaultTableModel model = (DefaultTableModel) tbKaryawan.getModel();
        model.setRowCount(0);
        Object[] row = new Object[11];
        for (int i = 0; i < data.size(); i++) {
            row[0] = data.get(i).id;
            row[1] = data.get(i).nip;
            row[2] = data.get(i).nama_lengkap;
            row[3] = data.get(i).username;
            row[4] = data.get(i).password;
            row[5] = data.get(i).email;
            row[6] = data.get(i).tanggal_lahir;
            row[7] = data.get(i).tanggal_bergabung;
            row[8] = data.get(i).status_nama;
            row[9] = data.get(i).nama_jabatan;
            row[10] = data.get(i).nama_shift;            
            model.addRow(row);
        }
    }    
    
